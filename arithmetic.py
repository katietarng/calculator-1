def add(num1, num2):
    """Input two numbers and return the sum""" 
    return num1 + num2

def subtract(num1, num2):
    """Input two numbers and return the difference"""
    return num1 - num2

def multiply(num1, num2):
    """Input two numbers and return the product"""
    return num1 * num2

def divide(num1, num2):
    """Input two numbers and return the quotient"""
    return float(num1)/num2

def square(num1):
    """Input one number and return the square"""
    return num1 * num1

def cube(num1):
    """Input one number and return the cube"""
    return num1 * num1 * num1

def power(num1, num2):
    """Input two numbers and return the num1 multiplied by num2 times"""
    return num1**num2

def mod(num1, num2):
    """Input two numbers and return the remainder"""
    return num1%num2
